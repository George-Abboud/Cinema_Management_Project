    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        Cinema cinema = new Cinema();
        String str = numberField.getText();
        if (str.length() == 0) {
            messageLabel.setText("Field can't be empty");
            return;
        } else if (str.length() > 10) {
            messageLabel.setText("Phone Number can't exceed 10 digits");
            return;
        } else if (str.length() < 10) {
            messageLabel.setText("Phone Number can't be less than 10 digits");
            return;

        } else if (str.charAt(0) != '0' || str.charAt(1) != '9') {
            messageLabel.setText("Phone Number must start wirh 09");
            return;
        }

        boolean valid = true;
        for (int i = 0; i < str.length(); i++) {
            if (!Character.isDigit(str.charAt(i))) {
                valid = false;
                break;
            }
        }
        if (valid == false) {
            messageLabel.setText("Phone Number can only contain digits");
        } else {
            if (Cinema.noOfVIPSeat == 0) {
                if (cinema.addTickets(Cinema.hallName, Cinema.movieName, Cinema.showTime, Cinema.noOfTickets, Cinema.passwordKey) == true) {
                    UserProfileFrame userProfileFrame = new UserProfileFrame();
                    PaymentCompletionFrame paymentCompletedFrame = new PaymentCompletionFrame();
                } else {
                    AddTicketFrame addTicketFrame = new AddTicketFrame();
                    addTicketFrame.failedLabel.setText("Booking Failed!");
                }
            } else {
                boolean ok = cinema.addTicketVIP(Cinema.hallName, Cinema.movieName, Cinema.showTime, Cinema.noOfVIPSeat, Cinema.passwordKey);
                if (ok == true) {
                    UserProfileFrame userProfileFrame = new UserProfileFrame();
                    PaymentCompletionFrame paymentCompletedFrame = new PaymentCompletionFrame();
                } else {
                    AddTicketFrame addTicketFrame = new AddTicketFrame();
                    addTicketFrame.failedLabel.setText("Booking Failed!");
                }
            }

            //Add Ticket Here
            this.dispose();
        }
    }//GEN-LAST:event_confirmButtonActionPerformed

